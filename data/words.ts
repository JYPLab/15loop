export function shuffledDailyWords(dateKey: string) {
  const words = [...dailyWords];
  let seed = Array.from(dateKey).reduce((sum, char) => sum + char.charCodeAt(0), 0);

  for (let index = words.length - 1; index > 0; index -= 1) {
    seed = (seed * 9301 + 49297) % 233280;
    const swapIndex = Math.floor((seed / 233280) * (index + 1));
    [words[index], words[swapIndex]] = [words[swapIndex], words[index]];
  }

  return words;
}
